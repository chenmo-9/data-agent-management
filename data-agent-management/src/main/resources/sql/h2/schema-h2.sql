CREATE TABLE IF NOT EXISTS agent (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description CLOB,
    avatar CLOB,
    category VARCHAR(100),
    tags CLOB,
    prompt CLOB,
    preset_questions CLOB,
    status VARCHAR(50) DEFAULT 'draft',
    admin_id BIGINT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_agent_name ON agent (name);
CREATE INDEX IF NOT EXISTS idx_agent_status ON agent (status);
CREATE INDEX IF NOT EXISTS idx_agent_category ON agent (category);
CREATE INDEX IF NOT EXISTS idx_agent_admin_id ON agent (admin_id);

CREATE TABLE IF NOT EXISTS model_config (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    provider VARCHAR(100) NOT NULL,
    model_name VARCHAR(255) NOT NULL,
    model_type VARCHAR(50) NOT NULL,
    base_url VARCHAR(1000),
    api_key VARCHAR(1000),
    temperature DOUBLE,
    max_tokens INT,
    enabled BOOLEAN DEFAULT TRUE,
    description CLOB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_model_config_name ON model_config (name);
CREATE INDEX IF NOT EXISTS idx_model_config_provider ON model_config (provider);
CREATE INDEX IF NOT EXISTS idx_model_config_model_type ON model_config (model_type);
CREATE INDEX IF NOT EXISTS idx_model_config_enabled ON model_config (enabled);

