CREATE TABLE IF NOT EXISTS semantic_table (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    datasource_id BIGINT NOT NULL,
    table_name VARCHAR(255) NOT NULL,
    business_name VARCHAR(255) NOT NULL,
    description CLOB,
    synonyms CLOB,
    enabled BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT unique_datasource_table UNIQUE (datasource_id, table_name)
);

CREATE INDEX IF NOT EXISTS idx_semantic_table_datasource_id ON semantic_table (datasource_id);
CREATE INDEX IF NOT EXISTS idx_semantic_table_enabled ON semantic_table (enabled);

CREATE TABLE IF NOT EXISTS semantic_field (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_id BIGINT NOT NULL,
    datasource_id BIGINT NOT NULL,
    table_name VARCHAR(255) NOT NULL,
    field_name VARCHAR(255) NOT NULL,
    business_name VARCHAR(255) NOT NULL,
    data_type VARCHAR(100),
    description CLOB,
    synonyms CLOB,
    example_value CLOB,
    primary_key BOOLEAN DEFAULT FALSE,
    nullable BOOLEAN DEFAULT TRUE,
    enabled BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT unique_table_field UNIQUE (table_id, field_name)
);

CREATE INDEX IF NOT EXISTS idx_semantic_field_table_id ON semantic_field (table_id);
CREATE INDEX IF NOT EXISTS idx_semantic_field_datasource_id ON semantic_field (datasource_id);
CREATE INDEX IF NOT EXISTS idx_semantic_field_enabled ON semantic_field (enabled);

CREATE TABLE IF NOT EXISTS semantic_relation (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    datasource_id BIGINT NOT NULL,
    source_table_id BIGINT NOT NULL,
    source_table_name VARCHAR(255) NOT NULL,
    source_field_id BIGINT NOT NULL,
    source_field_name VARCHAR(255) NOT NULL,
    target_table_id BIGINT NOT NULL,
    target_table_name VARCHAR(255) NOT NULL,
    target_field_id BIGINT NOT NULL,
    target_field_name VARCHAR(255) NOT NULL,
    relation_type VARCHAR(50) DEFAULT 'logical',
    join_type VARCHAR(50) DEFAULT 'INNER JOIN',
    description CLOB,
    enabled BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_semantic_relation_datasource_id ON semantic_relation (datasource_id);
CREATE INDEX IF NOT EXISTS idx_semantic_relation_source_table_id ON semantic_relation (source_table_id);
CREATE INDEX IF NOT EXISTS idx_semantic_relation_target_table_id ON semantic_relation (target_table_id);
CREATE INDEX IF NOT EXISTS idx_semantic_relation_enabled ON semantic_relation (enabled);

